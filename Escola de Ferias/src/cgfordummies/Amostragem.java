    public void abrir() throws FileNotFoundException, IOException{
        
        JFileChooser chooser = new JFileChooser();
        int returnVal = chooser.showOpenDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            fr = new FileReader(chooser.getSelectedFile().getAbsoluteFile());
            BufferedReader bf = new BufferedReader(fr);
            numLinhas = Integer.parseInt(bf.readLine());
            System.out.println(numLinhas + "");
            matriz = new int[numLinhas][numLinhas];
            for(int i = 0; i < numLinhas; i++){
                String linha[] = bf.readLine().split(" ");
                for(int j = 0; j < numLinhas; j++){
                    matriz[i][j] = Integer.parseInt(linha[j]);
                }
            }
            atualizar();
        }
    }
    
    
    public void atualizar(){
        Graphics g = jPanel1.getGraphics();
        g.clearRect(0, 0, 350, 350);
        for(int i = 0; i < numLinhas; i++){
            for(int j = 0; j < numLinhas; j++){
                if(matriz[i][j] == 1){
                    g.setColor(Color.blue);
                    g.drawRect(i, j, 1, 1);
                }
            }
        }
    }
    
    public void rotacionarMatrizHorario() {
        int largura = numLinhas;
        int altura = numLinhas;
        int[][] ret = new int[altura][largura];
            for (int i=0; i<altura; i++) {
                for (int j=0; j<largura; j++) {
                    ret[i][j] = matriz[largura - j - 1][i];
                }
            }
        matriz = ret;
        atualizar();
    }
    
    
